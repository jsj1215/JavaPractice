package com.practice.chapter4;

public class BinaryTest {

	public static void main(String[] args) {
		
		// int : 4byte
		
		int num = 10;
		//0B : 뒤에 숫자는 바이너리이다.
		int bNm = 0B1010;
		// 8진수
		int oNum = 012;
		//16진수
		int xNum = 0XA;
		
		System.out.println(num);
		System.out.println(bNm);
		System.out.println(oNum);
		System.out.println(xNum);
	}
}
